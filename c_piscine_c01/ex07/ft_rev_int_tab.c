void	ft_rev_int_tab(int *tab, int size)
{
	int	g;
	int	c;

	c = 0;
	if (size > 1)
	{
		while (c < size / 2)
		{
			g = tab[c];
			tab[c] = tab[size - c - 1];
			tab[size - c - 1] = g;
			c++;
		}
	}
}
